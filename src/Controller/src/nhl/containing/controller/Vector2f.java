package nhl.containing.controller;

/**
 * A 2-element vector that is represented by single-precision
 * floating point x, y, z coordinates.
 *
 * @author Niels
 */
public class Vector2f
{
    public float x;
    public float y;
    
    public Vector2f(float x, float y){
        this.x = x;
        this.y = y;
    }
}
